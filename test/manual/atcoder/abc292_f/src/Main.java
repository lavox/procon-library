	public void solve() {
		FastScanner sc = new FastScanner(System.in);
		int A = sc.nextInt();
		int B = sc.nextInt();
		int a = Math.min(A, B);
		int b = Math.max(A, B);
		double r = 0;
		if (a <= b * Math.sqrt(3) / 2) {
			r = a * 2 / Math.sqrt(3);
		} else {
			r = Bisect.maxTrueDouble(a, Math.sqrt(a * a + b * b), 1e-10, rr -> {
				double th = Math.acos(a / rr);
				double y = rr * Math.sin(th + Math.PI / 3);
				return y <= b;
			});
		}
		System.out.println(String.format("%.10f", r));
	}

